#include "kh.h"

/* \doc |kh_init| should be called at the start of "main()", and serves to
   initialize debugging and other support for \khepera. */

void kh_init( void )
{
   tim_start( "total" );
   _dbg_register( KH_FAREY,     "farey" );
   _dbg_register( KH_TRE,       "tre" );
   _dbg_register( KH_SRC,       "src" );
   _dbg_register( KH_PP,        "pp" );
   _dbg_register( KH_PPVERBOSE, "ppverbose" );
   _dbg_register( KH_LINE,      "line" );
   _dbg_register( KH_MEMORY,    "memory" );
   _dbg_register( KH_TIME,      "time" );
   _dbg_register( KH_RULES,     "rules" );
   _dbg_register( KH_REPLACE,   "replace" );
   _dbg_register( KH_SCOPE,     "scope" );

#ifndef __CHECKER__
#ifdef HAVE_ATEXIT
      atexit( kh_shutdown );
#else
# ifdef HAVE_ON_EXIT
      on_exit( kh_shutdown, NULL );
# endif
#endif
#endif
}

void kh_shutdown( void )
{
   if (dbg_test(KH_MEMORY)) {
      str_print_stats( stderr );
      src_print_stats( stderr );
      tre_print_stats( stderr );
   }

   src_destroy();
   str_destroy();
   _tre_shutdown();
   _lst_shutdown();
   _prs_shutdown();

   tim_stop( "total" );
   if (dbg_test(KH_TIME)) {
      tim_print_timers( stderr );
   }
   _tim_shutdown();
   dbg_destroy();
}

void kh_insert_notice( FILE *str, const char *name, const char *input )
{
   time_t t;

   time( &t );
   
   fprintf( str,
	    "/* ***** DO NOT EDIT THIS FILE *****\n"
	    " * This file (%s) was automatically generated by Khepera\n"
	    " * from %s on %s"
	    " */\n\n",
	    name, input, ctime( &t ) );
}
